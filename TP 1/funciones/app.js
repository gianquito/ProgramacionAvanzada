function cambiarColor(color) {
    if (color === 'rojo') {
        document.bgColor = 'red'
    } else if (color === 'azul') {
        document.bgColor = 'blue'
    } else if (color === 'verde') {
        document.bgColor = 'green'
    } else if (color === 'amarillo') {
        document.bgColor = 'yellow'
    }
}
